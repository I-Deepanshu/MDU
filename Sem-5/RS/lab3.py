# ### 1. **Arithmetic Operations**
# Arithmetic operations are used to perform basic mathematical calculations.

# #### Operations:
# - **Addition (+)**
# - **Subtraction (-)**
# - **Multiplication (*)**
# - **Division (/)**
# - **Floor Division (//)**
# - **Modulus (%)**
# - **Exponentiation (**)**
  
#### Example Code:

# Arithmetic Operations

a = 10
b = 5

# Addition
print("Addition:", a + b)

# Subtraction
print("Subtraction:", a - b)

# Multiplication
print("Multiplication:", a * b)

# Division
print("Division:", a / b)

# Floor Division
print("Floor Division:", a // b)

# Modulus
print("Modulus:", a % b)

# Exponentiation
print("Exponentiation:", a ** b)

#### Output:

# Addition: 15
# Subtraction: 5
# Multiplication: 50
# Division: 2.0
# Floor Division: 2
# Modulus: 0
# Exponentiation: 100000


### 2. **Comparison Operations**
# Comparison operations are used to compare values and return a Boolean value (`True` or `False`).

#### Operations:
# - **Equal to (==)**
# - **Not equal to (!=)**
# - **Greater than (>)**
# - **Less than (<)**
# - **Greater than or equal to (>=)**
# - **Less than or equal to (<=)**

#### Example Code:

# Comparison Operations

x = 20
y = 15

# Equal to
print("Equal to:", x == y)

# Not equal to
print("Not equal to:", x != y)

# Greater than
print("Greater than:", x > y)

# Less than
print("Less than:", x < y)

# Greater than or equal to
print("Greater than or equal to:", x >= y)

# Less than or equal to
print("Less than or equal to:", x <= y)

#### Output:

# Equal to: False
# Not equal to: True
# Greater than: True
# Less than: False
# Greater than or equal to: True
# Less than or equal to: False


### 3. **Logical Operations**
# Logical operations are used to combine multiple conditions.

#### Operations:
# - **AND (and)**
# - **OR (or)**
# - **NOT (not)**

#### Example Code:

# Logical Operations

p = True
q = False

# AND operation
print("AND operation:", p and q)

# OR operation
print("OR operation:", p or q)

# NOT operation
print("NOT operation:", not p)

#### Output:

# AND operation: False
# OR operation: True
# NOT operation: False



### 4. **Assignment Operations**
# Assignment operations are used to assign values to variables.

#### Operations:
# - **Simple Assignment (=)**
# - **Add and Assign (+=)**
# - **Subtract and Assign (-=)**
# - **Multiply and Assign (*=)**
# - **Divide and Assign (/=)**

#### Example Code:

# Assignment Operations

a = 5

# Simple Assignment
print("Simple Assignment:", a)

# Add and Assign
a += 2
print("Add and Assign:", a)

# Subtract and Assign
a -= 3
print("Subtract and Assign:", a)

# Multiply and Assign
a *= 4
print("Multiply and Assign:", a)

# Divide and Assign
a /= 2
print("Divide and Assign:", a)


#### Output:

# Simple Assignment: 5
# Add and Assign: 7
# Subtract and Assign: 4
# Multiply and Assign: 16
# Divide and Assign: 8.0


### 5. **String Operations**
# Strings are sequences of characters and Python provides various operations to manipulate them.

#### Operations:
# - **Concatenation (+)**
# - **Repetition (*)**
# - **Length (len())**

#### Example Code:

# String Operations

str1 = "Hello"
str2 = "World"

# Concatenation
print("Concatenation:", str1 + " " + str2)

# Repetition
print("Repetition:", str1 * 3)

# Length
print("Length of str1:", len(str1))

#### Output:

# Concatenation: Hello World
# Repetition: HelloHelloHello
# Length of str1: 5


